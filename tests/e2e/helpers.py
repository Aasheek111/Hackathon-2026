def set_local_storage_token(driver, base_url: str, token: str) -> None:
    """Logs the browser in without driving the login form - used by tests
    whose focus is a downstream page, not the login flow itself (that flow
    has its own dedicated test in test_auth_flow.py).
    """
    driver.get(base_url)
    driver.execute_script("window.localStorage.setItem('token', arguments[0]);", token)


def wait_until(predicate, timeout: float = 20.0, interval: float = 0.5, message: str = "condition not met"):
    deadline = time.time() + timeout
    last_exc = None
    while time.time() < deadline:
        try:
            if predicate():
                return True
        except Exception as exc:  # noqa: BLE001 - re-raised as a clear timeout below
            last_exc = exc
        time.sleep(interval)
    raise TimeoutError(f"{message} (waited {timeout}s)" + (f" - last error: {last_exc}" if last_exc else ""))
