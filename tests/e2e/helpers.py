def cleanup_subject(subject_id: str) -> None:
    _run_node_script(
        f"""
        const {{ PrismaClient }} = require('@prisma/client');
        const prisma = new PrismaClient();
        (async () => {{
          await prisma.subject.delete({{ where: {{ id: '{subject_id}' }} }}).catch(() => {{}});
          await prisma.$disconnect();
        }})();
        """
    )


